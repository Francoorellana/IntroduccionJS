//var, variables globales
var saludo="Hola Mundo"
if(saludo){
    var saludo = "Hola"
    console.log(saludo)
}
console.log(saludo)

//let, variables limitadas al bloque donde se encuentran definidas
let nombre = "Hello"
console.log(nombre)
if(nombre){
    let nombre = "CHAU"
    console.log(nombre)
}
//const, variables inmutables
const PI = 3.14
console.log(PI)
